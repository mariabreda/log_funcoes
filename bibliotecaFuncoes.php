namespace conversao {

    function dolarParaReal($valor, $cotacao)
    {
        return $valor * $cotacao;
    }

    function europarareal($valor, $cotacao)
    {
        return $valor * $cotacao;
    }

    function pesoParaReal($valor, $cotacao)
    {
        return $valor / $cotacao;
    }

    function  libraParaReal($valor, $cotacao)
    {
        return $valor / $cotacao;
    }

    function  ieneParaReal($valor, $cotacao)
    {
        return $valor / $cotacao;
    }
}
